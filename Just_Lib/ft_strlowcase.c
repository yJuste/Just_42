/*   â€¢ Sets a str to lowercase.                                               */
/*   â€¢ Prototype:   CHAR * ( char * )                                         */
/*        -> size_t                                                           */
/* ************************************************************************** */
#include "just_lib.h"

char	*ft_strlowcase(char *str)
{
	size_t		i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
	return (str);
}
