/*   • Sets a str to uppercase.                                               */
/*   • Prototype:   CHAR* ( char* str )                                       */
/*        -> size_t                                                           */
/* ************************************************************************** */
#include <stdlib.h>

char	*ft_strupcase(char *str)
{
	size_t		i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] -= 32;
		i++;
	}
	return (str);
}
