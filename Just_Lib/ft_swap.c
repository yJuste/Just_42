/*   • Swaps 2 num.                                                           */
/*   • Prototype:   VOID ( int *, int * )                                     */
/* ************************************************************************** */

void	ft_swap(int *a, int *b)
{
	int		x;

	x = *a;
	*a = *b;
	*b = x;
}

// Ajout de pointeur et change en chaine de caracteres
void	ft_swap_extra(char **a, char **b)
{
	char		*x;

	x = *a;
	*a = *b;
	*b = x;
}
