/*   • Calculates the greatest common divisor of 2 num.                       */
/*   • Prototype:   int ( int a, int b )                                      */
/* ************************************************************************** */

int	ft_pgcd(int a, int b)
{
	int		x;

	x = 0;
	while (b != 0)
	{
		x = b;
		b = a % b;
		a = x;
	}
	return (a);
}
