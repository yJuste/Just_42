/*   • Calculates the greatest common divisor of 2 num.                       */
/*   • Prototype:   SIZE_T ( size_t a, size_t b )                             */
/*        -> size_t                                                           */
/* ************************************************************************** */
#include <stdlib.h>

size_t	ft_pgcd(size_t a, size_t b)
{
	size_t		x;

	x = 0;
	while (b != 0)
	{
		x = b;
		b = a % b;
		a = x;
	}
	return (a);
}
