#include "../just_lib.h"

int	ft_str_is_printable(char *str)
{
	size_t		i;

	i = 0;
	while (str[i])
	{
		if (!(str[i] >= 32 && str[i] <= 126))
			return (0);
		i++;
	}
	return (1);
}
