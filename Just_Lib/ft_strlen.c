/*   • Calculates the len of a str.                                           */
/*   • Prototype:   INT ( int, int )                                          */
/* ************************************************************************** */

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}
