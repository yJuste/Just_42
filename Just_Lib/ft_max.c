/*   • Finds the maximum value of an arr.                                     */
/*   • Prototype:   INT ( int *, int )                                        */
/* ************************************************************************** */

int	max(int *tab, int len)
{
	int		res;
	int		i;

	i = 0;
	res = tab[i];
	while (i < len)
	{
		if (res < tab[i])
			res = tab[i];
		i++;
	}
	return (res);
}
