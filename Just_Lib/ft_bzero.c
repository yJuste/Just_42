/*   • Sets all bits to zero.                                                 */
/*   • Prototype:   VOID ( void *, size_t )                                   */
/*        -> size_t                                                           */
/* ************************************************************************** */
#include "just_lib.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		i;

	i = 0;
	while (i < n)
	{
		((char *)s)[i] = 0;
		i++;
	}
}
