/*   • Displays a char.                                                       */
/*   • Prototype:   VOID ( char c )                                           */
/*        -> write ( fd, buf, count )                                         */
/* ************************************************************************** */
#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}
