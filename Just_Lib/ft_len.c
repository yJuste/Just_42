/*   • Calculates the len of a num.                                           */
/*   • Prototype:   INT ( int nbr, int base_len )                             */
/* ************************************************************************** */

int	ft_len(int nbr, int base_len)
{
	int		len;
	long	n;

	len = 0;
	n = nbr;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n = -n;
		len++;
	}
	while (n > 0)
	{
		n /= base_len;
		len++;
	}
	return (len);
}
