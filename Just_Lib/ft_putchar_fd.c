/*   • Writes a char in a file.                                               */
/*   • Prototype:   VOID ( char, int )                                        */
/*        -> write                                                            */
/* ************************************************************************** */
#include "just_lib.h"

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}
