/*   • Parses 1 char if it is a space.                                        */
/*   • Prototype:   INT ( int )                                               */
/* ************************************************************************** */
#include "just_lib.h"

int	ft_isspace(int c)
{
	return ((c >= 9 && c <= 13) || c == 32);
}
