/*   • Returns the lower-case equivalent of an upper-case character.          */
/*   • Prototype:   INT ( int c )                                             */
/* ************************************************************************** */

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (c + 32);
	return (c);
}
