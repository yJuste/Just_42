/*   • Calculates the least common divisor of 2 num.                          */
/*   • Prototype:   INT ( int a, int b )                                      */
/* ************************************************************************** */

int	lcm(int a, int b)
{
	int		n;

	if (a == 0 || b == 0)
		return (0);
	if (a > b)
		n = a;
	else
		n = b;
	while (1)
	{
		if (n % a == 0 && n % b == 0)
			return (n);
		++n;
	}
}
