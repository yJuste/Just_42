/*   • Returns the upper-case equivalent of a lower-case char.                */
/*   • Prototype:   INT ( int )                                               */
/* ************************************************************************** */
#include "just_lib.h"

int	ft_toupper(int c)
{
	return (c - (c >= 'a' && c <= 'z') * 32);
}
