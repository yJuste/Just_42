/*   • Deletes a node.                                                        */
/*   • Prototype:   VOID ( struct *, f() )                                    */
/*        -> free                                                             */
/* ************************************************************************** */
#include "just_lib.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (lst && del)
	{
		del(lst->content);
		free(lst);
	}
}
