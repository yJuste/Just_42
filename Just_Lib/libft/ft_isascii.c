/*   • Parses 1 char if it is in ascii.                                       */
/*   • Prototype:   INT ( int )                                               */
/* ************************************************************************** */
#include "just_lib.h"

int	ft_isascii(int c)
{
	return (c >= 0 && c <= 127);
}
