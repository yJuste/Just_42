/*   • Applies a function to each char in a str.                              */
/*   • Prototype:   VOID ( char *, f() )                                      */
/* ************************************************************************** */
#include "just_lib.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	i = 0;
	if (!s)
		return ;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}
