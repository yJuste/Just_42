/*   • Writes str with '\n' in a file.                                        */
/*   • Prototype:   VOID ( char *, int )                                      */
/*        -> write                                                            */
/* ************************************************************************** */
#include "just_lib.h"

void	ft_putendl_fd(char *s, int fd)
{
	if (!s)
		return ;
	while (*s)
		write(fd, s++, 1);
	write(fd, "\n", 1);
}
