/*   • Returns 1 or 0 if is a char in a str.                                  */
/*   • Prototype:   INT ( char *, char )                                      */
/* ************************************************************************** */
#include "just_lib.h"

int	ft_ischar(char const *set, char c)
{
	size_t		i;

	i = 0;
	while (set[i])
	{
		if (set[i] == c)
			return (1);
		i++;
	}
	return (0);
}
