/*   • Sets a string to a character.                                          */
/*   • Prototype:   VOID * ( void *, int, size_t )                            */
/*        -> size_t                                                           */
/* ************************************************************************** */
#include "just_lib.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t		i;

	i = 0;
	while (i < len)
	{
		((unsigned char *)b)[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
