#include "just_lib.h"

int	ft_isprime(int n)
{
	int		i;

	i = 2;
	if (n <= 1)
		return (0);
	while (i < n && i > 0)
	{
		if (n % i == 0)
			return (0);
		i++;
	}
	return (1);
}
