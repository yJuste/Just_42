/*   • Returns the lower-case equivalent of an upper-case char.               */
/*   • Prototype:   INT ( int )                                               */
/* ************************************************************************** */
#include "just_lib.h"

int	ft_tolower(int c)
{
	return (c + (c >= 'A' && c <= 'Z') * 32);
}
