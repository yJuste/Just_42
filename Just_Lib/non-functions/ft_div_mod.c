/*   • Divides & mod.                                                         */
/*   • Prototype:   VOID ( int a, int b, int* div, int* mod )                 */
/* ************************************************************************** */

void	ft_div_mod(int a, int b, int *div, int *mod)
{
	*div = a / b;
	*mod = a % b;
}
