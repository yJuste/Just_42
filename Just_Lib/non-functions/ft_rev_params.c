#include "../just_lib.h"

int	main(int argc, char **argv)
{
	size_t		i;

	i = argc - 1;
	while (i > 0)
	{
		ft_putstr(argv[i]);
		ft_putstr("\n");
		i--;
	}
	return (0);
}
