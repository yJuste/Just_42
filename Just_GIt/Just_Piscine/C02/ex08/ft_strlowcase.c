#include <stdlib.h>

char	*ft_strlowcase(char *str)
{
	size_t		i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + 32;
		i++;
	}
	return (str);
}
