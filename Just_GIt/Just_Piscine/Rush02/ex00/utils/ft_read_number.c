#include "../libs/lib_string.h"
#include "../libs/lib_arg1.h"

void	read_number(char *number_str, size_t size)
{
	int	len;

	len = read(0, number_str, size - 1);
	if (len > 0)
		number_str[len - 1] = '\0';
}
