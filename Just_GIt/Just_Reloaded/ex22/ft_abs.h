#ifndef FT_ABS_H
# define FT_ABS_H

# define ABS(Value) \
({ \
	int	_val = (Value); \
	int	_abs; \
	if (_val < 0) \
		_abs = -_val; \
	else \
		_abs = _val; \
	_abs; \
})

#endif
